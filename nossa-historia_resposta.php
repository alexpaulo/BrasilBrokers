                                A história da Galvão Vendas de Imóveis Ltda. tem início em 1991, quando corretores da Comissária Galvão deixam a empresa para criar a Galvão Corretores de Imóveis Ltda. O objetivo era prestar serviço de compra e venda de imóveis para a Galvão e demais clientes. Com a experiência adquirida nos anos de trabalho, esta nova empresa conquistou a confiança do mercado de Curitiba.
                            </p>
                            <p>
                                Durante a crise ocorrida entre os anos de 1996 e 2001, a Galvão Corretores de Imóveis Ltda. passou por um momento de atividade moderada. Porém, no final de 2001, prevendo as mudanças que viriam no mercado curitibano, a atual diretoria assume a administração da empresa e começa a inovar. Além da  alteração do nome, que passa a ser Galvão Vendas de Imóveis Ltda., estabelece sede na Rua Comendador Fontana, 75, no Centro Cívico. Na seqüência, passa a investir em tecnologia e treinamento para desenvolvimento dos colaboradores. Então, a prática do trabalho diferenciado recebe destaque e conquista novos parceiros locais e nacionais para representar a venda de novas incorporações.
                            </p>
                            <p>
                                No início, a Galvão contava com apenas oito corretores, dois colaboradores, quatro computadores e uma carteira de 70 imóveis prontos. Hoje, são 700 corretores de lançamentos, 80 corretores de prontos para morar e três equipes especializadas em atendimento on-line. Além disso, são seis locais de atendimento e um setor administrativo focado em proporcionar todo o suporte necessário para o departamento comercial.
                            </p>
                            <p>
                                Além de vendas,  a Galvão oferece o serviço de Planejamento Imobiliário, onde participou e participa da criação de grandes sucessos no mercado de Curitiba. E, para não deixar de oferecer nenhuma solução no assunto imóvel, a Galvão Vendas possui uma parceria, um acordo operacional, com a Galvão Locações para indicar clientes interessados em administração ou locação de imóveis. A recíproca é verdadeira e clientes que procuram a Galvão Locações são indicados para a Galvão Vendas. Para facilitar o trabalho, como parte do acordo, ambas as empresas mantém atendentes de locação e corretores em suas lojas.
